#include "fillit.h"

int	ft_check_fit(int *dst, int *bin, int scol)
{
	int	i;

	i = 0;
	while (i < 4)
	{
		if ((bin[i] << scol) & *dst)
			return (0);
		i++;
		dst++;
	}
	return (1);
}
