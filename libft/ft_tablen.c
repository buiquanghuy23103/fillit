#include "libft.h"

size_t	ft_tablen(const void **tab)
{
	size_t	i;
	int		**itab;

	i = 0;
	if (tab && *tab)
	{
		itab = (int **) tab;
		while (itab[i])
			i++;
	}
	return (i);
}
