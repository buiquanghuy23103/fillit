#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	size_t	k;

	i = ft_strlen(s1);
	k = 0;
	while (s2[k] && k < n)
		s1[i++] = s2[k++];
	s1[i] = '\0';
	return (s1);
}
