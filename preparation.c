static void	ft_set_srow(t_tetr *storage)
{
	int	i;
	int	h;

	i = 0;
	h = 0;
	while (i < 16)
	{
		if (storage->tmino[storage->tcount][i] != 0)
		{
			storage->tmino[storage->tcount][SROW] = i;
			break ;
		}
		++i;
	}
	while (i < 16 && storage->tmino[storage->tcount][i] != 0)
	{
		h++;
		i++;
	}
	storage->tmino[storage->tcount][HEIGHT] = h;
}

static void	ft_set_storage(t_tetr *storage, int *tmp)
{
	int			i;
	uint16_t	value;

	value = 0;
	i = 0;
	while (i < 16)
	{
		if (tmp[i])
		{
			value |= 1 << (15 - i);
			storage->tmino[storage->tcount][i / 4] |= 1 << (3 - (i % 4));
		}
		++i;
	}
	ft_set_srow(storage);
	i = 0;
	while (i < 119)
	{
		if (value == g_valid[i])
			return ;
		++i;
	}
	ft_error();
}

static void	ft_check_elems(char *tmp, t_tetr *storage)
{
	int	i;
	int	j;
	int	t[16];

	i = -1;
	j = 0;
	while (++i < 16)
		t[i] = 0;
	i = -1;
	while (tmp[++i])
	{
		if (!((i + 1) % 5) || !((i + 1) % 21))
		{
			if (tmp[i] != '\n')
				ft_error();
			continue ;
		}
		if (tmp[i] == '#')
			t[j] = 1;
		else if (tmp[i] != '.')
			ft_error();
		j++;
	}
	ft_set_storage(storage, t);
	storage->tcount++;
}

void	ft_set_minsize(t_tetr *storage)
{
	int	i;
	int	j;

	i = storage->tmino[0][SIZE] + 1;
	j = 0;
	while (storage->tcount * 4 > i * i)
	{
		++i;
	}
	while (j < MAXTETRIMINOS)
	{
		storage->tmino[j][SIZE] = i;
		++j;
	}
}

void	ft_validate(int fd, t_tetr *storage)
{
	char	tmp[22];
	int		ret;
	int		last;

	last = 0;
	ret = read(fd, tmp, 21);
	tmp[21] = '\0';
	while (ret == 21 || ret == 20)
	{
		if (ret == 20)
		{
			last = 1;
			tmp[20] = '\n';
		}
		if (ret == 21)
			last = 0;
		ft_check_elems(tmp, storage);
		ret = read(fd, tmp, 21);
		if ((!last && !ret) || (ret && storage->tcount == 26))
			ft_error();
	}
	if (!storage->tcount || !(!ret && storage->tcount > 0))
		ft_error();
	if (storage->tcount > 4)
		ft_set_minsize(storage);
}
